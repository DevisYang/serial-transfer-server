HexBuffer HexStringToBytes(const std::string& hex) {
	const char* data = hex.c_str();
	HexBuffer buffer(hex.size() / 2);
	for (int i = 0; i < buffer.size; i++) {
		buffer.data[i] = (uint8_t)((H2B[data[2 * i]] << 4) | H2B[data[i * 2 + 1]]);
	}
	return buffer;
}
